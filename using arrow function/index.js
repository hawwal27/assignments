//ADDITION IN ARROW FUNCTION
const add = (a, b) => a + b;
console.log(add(5, 3)); 


//SUBTRACTION IN  ARROW FUNCTION
const sub = (a, b) => a - b;

console.log(sub(10, 4)); 

//POWER IN ARROW FUNCTION
const pow = (a, b) => a ** b;

console.log(pow(2, 3)); 


//DIVISION IN ARROW FUNCTION
const div = (a, b) => a / b;

console.log(div(10, 2)); 

//MULTIPLICATION IN ARROW FUNCTION
const mul = (a, b) => a * b;

console.log(mul(4, 5));


//HELLO WORLD IN ARROW FUNCTION
const greet = () => console.log("Hello world");

greet(); 



const greetUser = (name) => console.log(`Hello ${name}`);

greetUser("Alice"); 




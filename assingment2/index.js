// Function to add two integers
function add(a, b) {
    return a + b;
}

// Function to subtract two integers
function sub(a, b) {
    return a - b;
}

// Function to calculate the power of two integers
function pow(a, b) {
    return a ** b;
}

// Function to multiply two integers
function mul(a, b) {
    return a * b;
}

console.log("Addition:", add(3, 4));       
console.log("Subtraction:", sub(10, 3));     
console.log("Power:", pow(2, 3));            
console.log("Multiplication:", mul(5, 4));  
